//Operadores en JavaScript
// Operadores Aritmeticos

var datoQ = 10;
var datoK = 20;

// SUMA +
var suma = datoQ + datoK;
console.log('La suma de ' +datoQ+' + '+datoK+' es igual a: ', suma);

//RESTA -
var resta = datoQ - datoK;
console.log('La resta de '+datoQ+' - '+datoK+' es igual a: ', resta);

//MULTIPLICACION *
var multiplicaion = datoQ * datoK;
console.log('La multiplicacion de '+datoQ+' * '+datoK+' es igual a: ', multiplicacion);

//DIVISION /
var division = datoQ / datoK;
console.log('La division de '+datoQ+' / '+datoK+' es igual a: ', division);

//MODULO O RESIDUO %
var modulo = datoQ % datoK;
console.log('El modulo de '+datoQ+' % '+datoK+' es igual a: ', modulo);

//INCREMENTO ++
var incremento = datoQ;
incremento++;
